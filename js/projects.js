/* Helper functions exposed globally */
window.NEO_findProject = function(id) {
    return window.NEO_PROJECTS.find(p => p.id === id) || null;
};

window.NEO_getProjectIndex = function(id) {
    return window.NEO_PROJECTS.findIndex(p => p.id === id);
};

window.NEO_getNextProject = function(id) {
    const idx = window.NEO_getProjectIndex(id);
    if (idx === -1) return null;
    return window.NEO_PROJECTS[(idx + 1) % window.NEO_PROJECTS.length];
};

window.NEO_getPrevProject = function(id) {
    const idx = window.NEO_getProjectIndex(id);
    if (idx === -1) return null;
    const prevIdx = idx === 0 ? window.NEO_PROJECTS.length - 1 : idx - 1;
    return window.NEO_PROJECTS[prevIdx];
};
